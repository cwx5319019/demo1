#include<stdio.h>

int main(){
	int a1[5] = {1,3,5,7,9};
	int a2[5] = {2,4,6,8,10};
	int* ar[2] = {a1,a2};

	int b1[5] = {11,13,15,17,19};
	int b2[5] = {12,14,16,18,20};
	int* br[2] = {b1,b2};

	int** arr[2] = {ar,br};
/*
	要求，使用arr，访问数据，1,3,6,17,20
*/
/*
	arr->&ar
	*arr -> *&ar -> ar -> &a1
	**arr -> *&a1 -> a1 -> &1
	***arr -> *&1 -> 1
*/
	printf("%d\n",***arr);
/*
	arr -> &ar
	*arr -> *&ar -> ar -> &a1
	**arr -> *&a1 -> a1 -> &1
	(**arr+1) -> &1+1 -> &3
	*(**arr+1) -> *&3 -> 3
*/
	printf("%d\n",*(**arr+1));
/*
	arr -> &ar
	*arr -> &a1
	(*arr+1) -> &a1+1 -> &a2
	*(*arr+1) -> *&a2 -> a2 -> &2
	(*(*arr+1)+2) -> &2+2 -> &6
	*(*(*arr+1)+2) -> *&6 -> 6
*/
	printf("%d\n",*(*(*arr+1)+2));
/*
	arr -> &ar
	(arr+1) -> &br
	*(arr+1) -> *&br -> br -> &b1
	**(arr+1) -> *&b1 -> b1 -> &11
	(**(arr+1)+3) -> &11 + 3 -> &17
	*(**(arr+1)+3) -> *&17 -> 17
*/
	printf("%d\n",*(**(arr+1)+3));
/*
	arr -> &ar
	(arr+1) -> &br
	*(arr+1) -> *&br -> br -> &b1
	*(arr+1)+1 -> &b1+1 -> &b2
	*(*(arr+1)+1) -> *&b2 -> b2 -> &12
	*(*(arr+1)+1)+4 -> &12+4 -> &20
	*(*(*(arr+1)+1)+4) ->*&20 -> 20
*/
	printf("%d\n",*(*(*(arr+1)+1)+4));
	return 0;
}
