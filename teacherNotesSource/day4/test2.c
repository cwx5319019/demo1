/*
	从键盘输入2个非0整型数，x和y，让他作为一个点的x，y坐标，在不适用任何判断语句的情况，计算出该点位于第几象限
	使用比较运算符，逻辑运算符和赋值运算符即可实现
	4个象限当中，第1到3象限，x和y总共为负数的数量为0,1,2，第四象限单独计算
*/
#include<stdio.h>

int main(){
	int x = 0,y = 0;
	printf("请输入x，y轴坐标:");
	scanf("%d %d",&x,&y);
	while(getchar()!=10);
	int pos = (x<0) + (y<0) + 1 + (x>0&&y<0)*2;
	printf("该点处于第%d象限\n",pos);
	return 0;
}
